export const ColorsGrid = (color: string) => {
  if (color === 'Preta') return '#000'
  if (color === 'Laranja') return '#f24f00'
  if (color === 'Amarela') return '#ffdb58'
  if (color === 'Cinza') return '#d3d3d3'
  if (color === 'Azul') return '#4169e1 '
  if (color === 'Rosa') return '#ffcbdb'
  if (color === 'Bege') return '#F5F5DC'
}
